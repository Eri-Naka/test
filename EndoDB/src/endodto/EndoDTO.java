package endodto;

public class EndoDTO {

	private String item;
	private int price;

	public void setItemname(String item) {
		this.item = item;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getItem() {
		return item;
	}

	public int getPrice() {
		return price;
	}

}
